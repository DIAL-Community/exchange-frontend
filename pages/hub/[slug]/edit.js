import { useCallback } from 'react'
import { NextSeo } from 'next-seo'
import { useRouter } from 'next/router'
import { useIntl } from 'react-intl'
import { Tooltip } from 'react-tooltip'
import ResourceEdit from '../../../components/hub/ResourceEdit'
import Footer from '../../../components/hub/ResourceFooter'
import Header from '../../../components/hub/ResourceHeader'
import ClientOnly from '../../../lib/ClientOnly'

const EditResourcePage = () => {
  const { formatMessage } = useIntl()
  const format = useCallback((id, values) => formatMessage({ id }, values), [formatMessage])

  const { locale, query: { slug } } = useRouter()

  return (
    <>
      <NextSeo
        title={format('ui.resource.header')}
        description={
          format(
            'shared.metadata.description.listOfKey',
            { entities: format('ui.resource.header')?.toLocaleLowerCase() }
          )
        }
      />
      <ClientOnly>
        <Header />
        <Tooltip id='react-tooltip' className='tooltip-prose z-20' />
        <ResourceEdit slug={slug} locale={locale} />
        <Footer />
      </ClientOnly>
    </>
  )
}

export default EditResourcePage
